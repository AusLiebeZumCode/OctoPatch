namespace OctoPatch
{
    /// <summary>
    /// Basic description class for all kind of descriptions
    /// </summary>
    public abstract class Description
    {
        /// <summary>
        /// Name
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// optional Description
        /// </summary>
        public string Description { get; set; }
    }
}
